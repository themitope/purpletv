            In 2020 at the peak of Covid-19, while working for a global food manufacturing company as the route-to-market manager, I was leading a team on fieldwork in a suburb of Abuja (FCT). I met a retailer who had just finished packaging some items waiting for delivery through ‘Aboki’ (a bike man). After several discussions, she revealed that most of her sales come from her online presence on WhatsApp status or Instagram, but a major challenge for her is delivery. Most of the ‘abokis’ were not reliable, and she has no control over overpricing. The second issue for her was trust, could she trust the delivery guys enough to deliver to the exact address? <br>
            On getting back to Lagos, I spoke with my partner, and we did further research in Ibadan (Agbeni, Bodija, Oluyole) and Lagos (Surulere, Ikeja). Out of the 50 retailers that we spoke to, all of them said they use smartphones and 80% use either WhatsApp or Facebook. In fact, bigger retailers have dedicated booths for WhatsApp sales and delivery, but they face similar challenges- they would appreciate a reliable and trusted platform to compare prices for delivery as well as increase their sales. <br>
            Our first prototype was a WhatsApp platform under Lurvity Logistics brand. We aggregated major last-mile logistics companies that operate on the mainland and built a database of retailers within the Surulere/Ikeja axis. We then acted as the connector for all their delivery services. The retailers on our WhatsApp platform get price comparisons and select the logistics company of their choice. We then follow up for final delivery. This concept test has been largely successful with 95% order fulfilment rate. <br>
